# Day 2 — Examples
# Variables and Data Types

# Storing text inside variables
name = "Alex"
city = "Chennai"

print(name)
print(city)

# Storing numbers
age = 18
score = 95

print(age)
print(score)

# Decimal numbers
height = 5.9

print(height)

# Boolean values
is_student = True

print(is_student)

# Using multiple variables together
favorite_game = "Minecraft"
hours_played = 120

print("Favorite Game:", favorite_game)
print("Hours Played:", hours_played)

# Variable reassignment
x = 7

# Variables in Python can be updated by assigning them a new value.
x = 6 + 7

print(x)

# Calculations with variables
a = 10
b = 5

sum_result = a + b
difference = a - b
product = a * b

print(sum_result)
print(difference)
print(product)

# Creating a simple profile
name = "Sarah"
age = 21
country = "India"
hobby = "Photography"

print("Name:", name)
print("Age:", age)
print("Country:", country)
print("Hobby:", hobby)

# Updating variable values
coins = 100

print("Starting Coins:", coins)

coins = coins + 50

print("Updated Coins:", coins)

# Slightly more advanced example
math_marks = 85
science_marks = 90
english_marks = 88

total_marks = math_marks + science_marks + english_marks

print("Total Marks:", total_marks)

# Final message
print("Day 2 completed successfully!")